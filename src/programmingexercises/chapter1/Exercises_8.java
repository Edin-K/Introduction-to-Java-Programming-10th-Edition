/**
 *  (Area and perimeter of a circle)Write program that displays the area and perimeter
 *  of a circle that has a radius of 5.5 using the following formula:
 *  perimeter = 2 * radius * PI
 *  area = radius * radius * PI
 *  
 *  @uthor Edin Korkic  
 */

public class Exercises_8 {

	public static void main(String[] args) {

		System.out.println(" Perimeter of a circle:" + 2 * 5.5 * 3.14159);
		System.out.println(" Area of a circle:" + 5.5 * 5.5 * 3.14159);
	
	}

}
